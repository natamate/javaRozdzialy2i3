
public class Cw9Str91 {
	public static void main(String[]args){
		
		char znak = 'a';
		Character objZnak = new Character(znak);
		System.out.println(znak);
		System.out.println(objZnak);
		
		boolean logiczna = true;
		Boolean objLogiczna = new Boolean(logiczna);
		System.out.println(logiczna);
		System.out.println(objLogiczna);
		
		short krotki = 3;
		Short objKrotki = new Short(krotki);
		System.out.println(krotki);
		System.out.println(objKrotki);
		
		int calkowita = 5;
		Integer objCalkowita = new Integer(calkowita);
		System.out.println(calkowita);
		System.out.println(objCalkowita);
		
		long dlugi = 7;
		Long objDlugi = new Long(dlugi);
		System.out.println(dlugi);
		System.out.println(objDlugi);
		
		double rzeczywista = 654.321;
		Double objRzeczywista = new Double(rzeczywista);
		System.out.println(rzeczywista);
		System.out.println(objRzeczywista);
		
		float krotkaRzeczywista = 123.456f;
		Float objKrotkaRzeczywista = new Float(krotkaRzeczywista);
		System.out.println(krotkaRzeczywista);
		System.out.println(objKrotkaRzeczywista);
		
		String slowo = "slowo";
		String objSlowo = new String(slowo);
		System.out.println(slowo);
		System.out.println(objSlowo);		
	}
}
